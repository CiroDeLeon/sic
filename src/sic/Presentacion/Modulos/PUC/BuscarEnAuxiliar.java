    @Override
    public void EventoDeSeleccionCtaEnPUC() {        
            if(this.bp.ObtenerCtaSeleccionada().getId().toString().length()==8){
                this.ctapuc=bp.ObtenerCtaSeleccionada();
                this.setTitle(ctapuc.getId()+"==>"+ctapuc.getDenominacion());
                this.bp.dispose();              
                
                if(ctat!=null && this.service.ObtenerCtaPuc(ctapuc.getId()+""+ctat.getId())!=null){
                    this.jButton2.setEnabled(false);
                    this.jMenuItem1.setEnabled(false);
                    this.InicializarCtaAuxiliar();
                    this.suscripcion.EventoDeSeleccionAuxiliar();
                }else{
                    this.show();    
                    this.jTextField1.setText("");
                    if(this.ctapuc.isPublico()==false){
                        this.jButton2.setEnabled(false);
                        this.jMenuItem1.setEnabled(false);
                    }
                }
                
                this.Buscar();
            }else{
                JOptionPane.showMessageDialog(this.bp,"Debe Ser Un  Aux. Contable");
            }            
    }

