    void Buscar(){
        String busqueda=this.jTextField1.getText();
        m=new Cta_PUCModel();        
        if(ctapuc!=null){        
           m.setLista(service.InspeccionarAuxiliar(busqueda,this.ctapuc.getId()));        
        }
        this.jTable1.setModel(m);                
        this.setAnchoColumnas();
    }
    void Seleccionar(){
        if(this.filaSeleccionada!=-1){
           this.ctaauxiliar=m.getRow(this.filaSeleccionada);
           if(this.suscripcion!=null)
           this.suscripcion.EventoDeSeleccionAuxiliar();
        }else{
            JOptionPane.showMessageDialog(this,"debes escoger una fila");
        }
    }
    void SeleccionarCtaEnPUC(){        
        bp=new BuscarEnPUC();
        bp.show();
        bp.setSuscripcion(this);
        this.dispose();    
        this.jTextField1.requestFocus();
        this.Buscar();
    }
    public Cta_PUC ObtenerAuxiliarSeleccionado(){
        return this.ctaauxiliar;
    }
    void IngresarEnAuxiliar(){
        System.out.println(ctat.getDescripcion()+" "+ctat.getId());
        this.service.IngresarEnElPUC(this.ctapuc.getId()+""+ctat.getId(),ctat.getDescripcion(),this.ctapuc.getTipoCta(),this.ctapuc.isRequiereNit(),ctat,ctapuc.isPublico(),ctapuc.getCategoria());        
        this.jButton2.setEnabled(false);
        this.Buscar();      
        if(this.suscripcion!=null){
            this.InicializarCtaAuxiliar();
            this.suscripcion.EventoDeSeleccionAuxiliar();
        }
    }
    void InicializarCtaAuxiliar(){
        ctaauxiliar=new  Cta_PUC();
            this.ctaauxiliar.setId(this.ctapuc.getId()+""+ctat.getId());
            this.ctaauxiliar.setCtat(ctat);
            this.ctaauxiliar.setDenominacion(ctat.getDescripcion());
            this.ctaauxiliar.setTipoCta(this.ctapuc.getTipoCta());
            this.ctaauxiliar.setRequiereNit(this.ctapuc.isRequiereNit());            
    }
    public void setAnchoColumnas(){       
        JViewport scroll =  (JViewport) this.jTable1.getParent();
        int ancho = scroll.getWidth();
        int anchoColumna=0;
        TableColumnModel modeloColumna = this.jTable1.getColumnModel();
        TableColumn columnaTabla;
        for (int i = 0; i < this.jTable1.getColumnCount(); i++) {
            columnaTabla = modeloColumna.getColumn(i);
            switch(i){
                case 0: anchoColumna = (15*ancho)/100;
                        break;
                 case 1: anchoColumna = (60*ancho)/100;
                        break;       
                case 2: anchoColumna = (15*ancho)/100;
                        break;           
                case 3: anchoColumna = (10*ancho)/100;
                        break;              
            }                     
            columnaTabla.setPreferredWidth(anchoColumna);           
        }
    }  
