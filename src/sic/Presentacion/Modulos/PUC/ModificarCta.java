    @Override
    public void EventoDeSeleccionCtaEnPUC() {
        this.cta=bp.ObtenerCtaSeleccionada();
        this.jTextField1.setText(cta.getId().toString());
        this.jTextField2.setText(cta.getDenominacion());
        this.jComboBox1.setSelectedItem(cta.getTipoCta());
        this.jRadioButton1.setSelected(cta.isRequiereNit());
        this.jRadioButton2.setSelected(!cta.isRequiereNit());
        this.jRadioButton3.setSelected(cta.isPublico());
        this.jRadioButton4.setSelected(!cta.isPublico());
        this.jComboBox2.setSelectedItem(cta.getCategoria());
        this.bp.dispose();
        this.show();
    }

