    void Guardar(){                    
              String denominacion=this.jTextField2.getText();
              Object id=this.jTextField1.getText();
              String tipo=this.jComboBox1.getSelectedItem().toString();
              boolean requierenit=this.jRadioButton1.isSelected(); 
              boolean publico=this.jRadioButton3.isSelected();
              String categoria=this.jComboBox2.getSelectedItem().toString();              
              if(service.ModificarCTA(id, denominacion, tipo, requierenit, null,publico,categoria)){
                    this.Limpiar();
              }              
              JOptionPane.showMessageDialog(this,service.ObtenerMensaje());
    }
    void BuscarEnPUC(){
        this.bp=new BuscarEnPUC();
        bp.setSuscripcion(this);
        this.dispose();
        this.bp.show();
    }
    void Limpiar(){
        this.jTextField1.setText("");
        this.jTextField2.setText("");
        this.jComboBox1.setSelectedIndex(0);
        this.jRadioButton1.setSelected(false);
        this.jRadioButton2.setSelected(false);
        this.jComboBox2.setSelectedIndex(0);
    }

