    @Override
    public void EventoDeSeleccionCtaEnPUC() {
        if(bp.ObtenerCtaSeleccionada().getId().toString().length()==8){
            this.setCta_PUC(bp.ObtenerCtaSeleccionada());
            bp.dispose();
            this.show();
        }else{
           JOptionPane.showMessageDialog(this.bp,"Debe Ser Un Auxiliar (8 Digitos)");             
        }
    }

