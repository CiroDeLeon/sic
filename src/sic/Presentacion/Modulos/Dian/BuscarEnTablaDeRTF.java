    void Limpiar(){        
        this.setCta_PUC(null);
    }
    void BuscarEnPUC(){
        bp=new BuscarEnPUC();
        bp.show();
        bp.setSuscripcion(this);
        this.dispose();
    }
    void Cargar(){
        modelo=new RetencionDianModel();
        int año=((Integer)this.jFormattedTextField1.getValue()).intValue();
        modelo.setLista(service.getDao().ObtenerTablaDeRetenciones(año));
        this.jTable1.setModel(modelo);
        this.jTable1.repaint();
    }
    void EventoSeleccionDeFila(){                
        this.Seleccionar();
    }
    void Seleccionar(){
        fila=this.jTable1.getSelectedRow();
        if(fila!=-1){
        seleccionado=modelo.getRow(fila);
        if(suscripcion!=null){
           suscripcion.EventoSeleccionDeTablaRTF();
        }
        }else{
           JOptionPane.showMessageDialog(this,"Debes Escoger Una Fila");             
        }
    }
