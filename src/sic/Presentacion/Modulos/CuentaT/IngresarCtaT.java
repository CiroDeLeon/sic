    void Guardar(){
        if(this.jTextField1.getText().trim().equals("")==false){
           if(service.InsertarCtaT(this.jTextField1.getText())==true){
               this.Limpiar();
           }
           JOptionPane.showMessageDialog(this,service.ObtenerMensaje());                       
        }else{
           JOptionPane.showMessageDialog(this,"No se Admiten Campos vacios");            
        }
    }
