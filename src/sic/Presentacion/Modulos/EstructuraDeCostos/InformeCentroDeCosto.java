    java.util.Date getFecha(DateChooserCombo d,int hora,int minuto){
        Date f=d.getSelectedDate().getTime();
        Date fecha=new java.util.Date(f.getYear(),f.getMonth(),f.getDate());
        long hora_en_segundos=hora*60*60;
        long minutos_en_segundos=minuto*60;
        long t=(hora_en_segundos+minutos_en_segundos)*1000;
        return new java.util.Date(fecha.getTime()+t);
    } 
    void Cargar(){      
        Date fecha_inicial=this.getFecha(dateChooserCombo1,this.jComboBox3.getSelectedIndex(),this.jComboBox4.getSelectedIndex());        
        Date fecha_final=this.getFecha(dateChooserCombo2,this.jComboBox5.getSelectedIndex(),this.jComboBox6.getSelectedIndex());        
        double val=service.getDao().ObtenerSaldoEstructuraDeCosto(subcontabilidad.getEstructura_de_costos().getId(),subcontabilidad.getId(),fecha_inicial,fecha_final);
        DefaultMutableTreeNode root=new DefaultMutableTreeNode("<>"+subcontabilidad.getDescripcion()+"<>"+NumberFormat.getInstance().format(val));        
        tree=new javax.swing.JTree(service.ObtenerHijos(subcontabilidad,root,service.getDao().ObtenerPadres(subcontabilidad.getEstructura_de_costos().getId()),fecha_inicial,fecha_final));                
        tree.addTreeSelectionListener(this);
        scrollPaneArbol = new JScrollPane();
        scrollPaneArbol.setBounds(2,3,700, 500);
        scrollPaneArbol.setViewportView(tree);        
        if(jd!=null){
            jd.dispose();
        }        
        jd=new JDialog();   
        tree.addKeyListener(this);
        tree.getSelectionModel().setSelectionMode(TreeSelectionModel.SINGLE_TREE_SELECTION);
        jd.add(this.scrollPaneArbol);
        jd.setBounds(0,0, 701, 501);
        jd.setVisible(true);
        jd.setTitle("<>"+this.subcontabilidad.getId()+"<>"+this.subcontabilidad.getDescripcion());
        jd.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
    }
