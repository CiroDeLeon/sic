    @Override
    public void EventoDeSeleccionDeSubcontabilidad() {
       if(bs.getSeleccionado().getEstructura_de_costos()!=null) {
          this.setSubcontabilidad(bs.getSeleccionado());
          bs.dispose();
          this.show();
       }else{
          JOptionPane.showMessageDialog(this,"Esta Subcontabilidad No Tiene Estructura de Costos Asociada");
       }
    }

    @Override
    public void setSubcontabilidad(Subcontabilidad subcontabilidad) {
        this.subcontabilidad=subcontabilidad;
        if(this.subcontabilidad!=null){
            this.jTextField1.setText(""+this.subcontabilidad.getId());
            this.jTextField2.setText(""+this.subcontabilidad.getDescripcion());
        }else{
            this.jTextField1.setText("");
            this.jTextField2.setText("");
        }
    }

    @Override
    public void valueChanged(TreeSelectionEvent e) {        
        Object[] path=e.getPath().getPath();
        nodo=path[path.length-1].toString();
        padre=null;
        if(path.length-2>=0){
            padre=path[path.length-2].toString();
        }
     
    }

    @Override
    public void keyTyped(KeyEvent e) {
        
    }

    @Override
    public void keyPressed(KeyEvent e) {
        if(e.getKeyCode()==KeyEvent.VK_ENTER){
            Date fecha_inicial=this.getFecha(dateChooserCombo1,this.jComboBox3.getSelectedIndex(),this.jComboBox4.getSelectedIndex());        
            Date fecha_final=this.getFecha(dateChooserCombo2,this.jComboBox5.getSelectedIndex(),this.jComboBox6.getSelectedIndex());        
            service.VerReporte(nodo, padre,this.subcontabilidad.getId(),fecha_inicial,fecha_final);
        }
    }

    @Override
    public void keyReleased(KeyEvent e) {
        
    }
